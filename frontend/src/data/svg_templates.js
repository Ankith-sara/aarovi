// Get templates by gender
export const getTemplatesByGender = (gender) => {
  return Object.entries(SVG_TEMPLATES)
    .filter(([_, template]) => template.gender === gender.toLowerCase())
    .reduce((acc, [key, value]) => ({ ...acc, [key]: value }), {});
};

// Get all template names
export const getAllTemplateNames = () => {
  return Object.keys(SVG_TEMPLATES);
};

// Get template by name
export const getTemplateByName = (name) => {
  return SVG_TEMPLATES[name] || SVG_TEMPLATES.Kurti;
};

// Get women's templates
export const getWomensTemplates = () => getTemplatesByGender('women');

// Get men's templates  
export const getMensTemplates = () => getTemplatesByGender('men');